int main()
{
    /* Init board hardware. */
    /* attach FRO 12M to FLEXCOMM4 (debug console) */
    CLOCK_SetClkDiv(kCLOCK_DivFlexcom4Clk, 1u);
    CLOCK_AttachClk(BOARD_DEBUG_UART_CLK_ATTACH);

    BOARD_InitPins();
    BOARD_InitBootClocks();
    BOARD_InitDebugConsole();

    TRDC_Type *baseAddr = (TRDC_Type *)TRDC_BASE;

    PRINTF("\r\nFlash Hiding example...\r\n");
    // Hiding for the third 32kb region of the MBC0_DOM_MEM0_BLK_CFG_W0
    // The third 32k region's address range:0x10000 ~ 0x18000
    bool flashHidingTestResult = flash_hiding_test((uint32_t *)(baseAddr->MBC_INDEX->MBC_DOM0_MEM0_BLK_CFG_W), 3);
    if (flashHidingTestResult == true)
    {
        PRINTF("flash hiding test erase and program PASSED\n");
    }
    else
    {
        PRINTF("flash hiding test erase and program FAILED\n");
    }

    PRINTF("\r\nCDPA example...\r\n");
    // CDPA for the fourth 32kb region of the MBC0_DOM_MEM0_BLK_CFG_W0
    // The fourth 32k region's address range:0x18000 ~ 0x20000
    // If the region is set to CDPA, both write and erase operations to this region will result in hardfault
    bool flashCDPATestResult = flash_CDPA_test((uint32_t *)(baseAddr->MBC_INDEX->MBC_DOM0_MEM0_BLK_CFG_W), 4);
    if (flashCDPATestResult)
    {
        PRINTF("CDPA test erase and program PASSED\n");
    }
    else
    {
        PRINTF("CDPA test erase and program FAILED\n");
    }
    while (1)
    {
    }
}

