bool flash_CDPA_test(uint32_t *CDPABlock, uint32_t CDPAPageId)
{
    *CDPABlock        = 0x0;
    bool testResult   = false;
    status_t status   = kStatus_Success;
    uint32_t CDPAaddr = 0x18000;
    uint32_t readBuffer[0x200];

    do
    {
        FlashConfig2.modeConfig.sysFreqInMHz = 96;

        status = FLASH_Init(&FlashConfig2);
        if (status != kStatus_FLASH_Success)
        {
            PRINTF("FLASH_Init FAILED\n");
            break;
        }
        else
        {
            PRINTF("FLASH_Init PASSED\n");
        }

        status = FFR_Init(&FlashConfig2);
        if (status != kStatus_FLASH_Success)
        {
            PRINTF("FFR_Init FAILED\n");
            break;
        }
        else
        {
            PRINTF("FFR_Init PASSED\n");
        }

        PRINTF("Before enabling the CDPA, call flash_erase API and flash_program API ...\n");
        PRINTF("Erasing FLASH sectors, address: %x, size:%x\n", CDPAaddr, REMAP_IMAGE_SIZE);
        status = FLASH_Erase(&FlashConfig2, CDPAaddr, REMAP_IMAGE_SIZE, kFLASH_ApiEraseKey);
        if (status != kStatus_Success)
        {
            PRINTF("flash_page_erase returned error:%d\n", status);
            return testResult;
        }
        else
        {
            PRINTF("flash_page_erase PASSED\n");
        }

        PRINTF("Programming FLASH, address: %x, size: %x\n", CDPAaddr, sizeof(RemapImageData));
        status = FLASH_Program(&FlashConfig2, CDPAaddr, (uint8_t *)&RemapImageData, sizeof(RemapImageData));
        if (status != kStatus_Success)
        {
            PRINTF("flash_program returned error:%d\n", status);
            return testResult;
        }
        else
        {
            PRINTF("CDPA no enabled and flash_program PASSED\n");
        }

        PRINTF("CDPABlock = %x\n", *CDPABlock);
        *CDPABlock = 0x4 << ((CDPAPageId - 1) * 4);
        PRINTF("CDPABlock = %x\n", *CDPABlock);

        status = FLASH_Read(&FlashConfig1, CDPAaddr, (uint8_t *)readBuffer, sizeof(readBuffer));
        if (status != kStatus_Success)
        {
            PRINTF("read FAILED\n");
            break;
        }
        else
        {
            PRINTF("CDPA enabled and read test PASSED\n");
        }
        testResult = true;

    } while (0);

    return testResult;
}

bool flash_hiding_test(uint32_t *MBCAddr, uint32_t MBCWordRegionId)
{
    bool testResult          = false;
    status_t status          = kStatus_Success;
    uint32_t flashHidingaddr = 0x10000;

    uint32_t readBuffer[0x200];

    do
    {
        FlashConfig1.modeConfig.sysFreqInMHz = 96;

        status = FLASH_Init(&FlashConfig1);
        if (status != kStatus_FLASH_Success)
        {
            PRINTF("FLASH_Init FAILED\n");
            break;
        }
        else
        {
            PRINTF("FLASH_Init PASSED\n");
        }
        status = FFR_Init(&FlashConfig1);
        if (status != kStatus_FLASH_Success)
        {
            PRINTF("FFR_Init FAILED\n");
            break;
        }
        else
        {
            PRINTF("FFR_Init PASSED\n");
        }

        PRINTF("Erasing FLASH sectors, address: %x, size:%x\n", flashHidingaddr, REMAP_IMAGE_SIZE);
        status = FLASH_Erase(&FlashConfig1, flashHidingaddr, REMAP_IMAGE_SIZE, kFLASH_ApiEraseKey);
        if (status != kStatus_Success)
        {
            PRINTF("flash_page_erase returned error:%d\n", status);
            return testResult;
        }
        else
        {
            PRINTF("flash_page_erase PASSED\n");
        }

        PRINTF("Programming FLASH, address: %x, size: %x\n", flashHidingaddr, sizeof(RemapImageData));
        status = FLASH_Program(&FlashConfig1, flashHidingaddr, (uint8_t *)&RemapImageData, sizeof(RemapImageData));
        if (status != kStatus_Success)
        {
            PRINTF("flash_page_program returned error:%d\n", status);
            return testResult;
        }
        else
        {
            PRINTF("flash_page_program PASSED\n");
        }

        status = FLASH_Read(&FlashConfig1, flashHidingaddr, (uint8_t *)readBuffer, sizeof(readBuffer));
        if (status == kStatus_FLASH_ReadHidingAreaDisallowed)
        {
            PRINTF("FLASH hiding enabled and read linmit!\n");
            break;
        }
        else
        {
            PRINTF("FLASH hiding no enabled and read PASSED!\n");
        }
        PRINTF("MBCAddr = %x\n", *MBCAddr);
        *MBCAddr = 0x5 << ((MBCWordRegionId - 1) * 4);
        PRINTF("MBCAddr = %x\n", *MBCAddr);
        status = FLASH_Read(&FlashConfig1, flashHidingaddr, (uint8_t *)readBuffer, sizeof(readBuffer));
        if (status != kStatus_FLASH_ReadHidingAreaDisallowed)
        {
            PRINTF("FLASH hiding disabled and read test FAILED\n");
            break;
        }
        else
        {
            PRINTF("FLASH hiding enabled and read limit PASSED\n");
        }

        testResult = true;

    } while (0);

    return testResult;
}
